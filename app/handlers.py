# Заглушка логики обработки входящего запроса (демо)
def handle_user_message(message):
    if "цена" in message.lower():
        return "Абонемент на месяц — от 2500 ₽"
    elif "расписание" in message.lower():
        return "Групповые тренировки проходят с 9:00 до 21:00 ежедневно"
    else:
        return "Передаю вопрос менеджеру. Ожидайте, пожалуйста."
